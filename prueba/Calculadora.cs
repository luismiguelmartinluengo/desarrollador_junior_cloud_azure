namespace prueba;

public class Calculadora(){

    public int sum(int _a, int _b){
        return _a + _b;
    }//End Sum

    public int subtraction(int _a, int _b){
        return sum(_a, -_b);
    }//End Resta

    public int multiplication(int _a, int _b){
        return _a * _b;
    }//End multiplication

    public int division(int _a, int _b){
        return _a / _b;
    }//End division


}//End Calculadora